# Aufgabe: Funktion mit Berechnung

# Teil 1: Definiere eine Funktion mit zwei Parametern
# Lücke 1: Definiere eine Funktion namens 'func', die zwei Parameter 'x' und 'y' annimmt
def func(x, y):
    # Lücke 2: Führe die gegebene Berechnung durch und gib das Ergebnis zurück
    return ((y // 2 - 1) ** x)

# Teil 2: Rufe die Funktion 'func' mit den Argumenten 2 und 7 auf und gib das Ergebnis aus
# Lücke 3: Rufe die Funktion 'func' mit den Argumenten 2 und 7 auf und speichere das Ergebnis in 'ergebnis'
ergebnis = func(2, 7)

# Teil 3: Gib das Ergebnis der Funktion auf der Konsole aus
# Lücke 4: Gib den Wert von 'ergebnis' auf der Konsole aus
print(ergebnis)
